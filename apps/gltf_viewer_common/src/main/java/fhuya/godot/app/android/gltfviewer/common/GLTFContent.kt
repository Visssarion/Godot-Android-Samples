    /**
     * Reference to a gltf asset.
     */
    data class GLTFItem(val name: String) {

        /**
         * The path is relative to the 'assets' directory.
         */
        val glbFilepath = "res://gltfs/food_kit/$name.glb"

        fun getScreenshotDrawableId(context: Context): Int {
            return context.resources.getIdentifier(
                name.lowercase(),
                "drawable",
                context.packageName)
        }

        override fun toString(): String = name
    }
